let currentLandingLang = 'en';

// Get translation
function tLanding(key) {
    const translation = LANDING_TRANSLATIONS[key];
    if (!translation) {
        console.warn(`[i18n-landing] Missing key: ${key}`);
        return key;
    }
    return translation[currentLandingLang] || translation['en'] || key;
}

// Set language
function setLandingLanguage(langCode) {
    if (LANDING_LANGUAGES[langCode]) {
        currentLandingLang = langCode;
        localStorage.setItem('siteLang', langCode);
        updateLandingTranslations();
        
        // Update lang button
        const langBtn = document.getElementById('lang-selector-btn');
        if (langBtn) {
            langBtn.textContent = LANDING_LANGUAGES[langCode].flag;
        }
        
        console.log(`[i18n-landing] Language set to: ${langCode}`);
    }
}

// Update all translations on page
function updateLandingTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = tLanding(key);
        if (translation) {
            el.textContent = translation;
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const translation = tLanding(key);
        if (translation) {
            el.placeholder = translation;
        }
    });
}

// Auto-detect language
function detectLandingLanguage() {
    // 1. Check URL parameter
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    if (urlLang && LANDING_LANGUAGES[urlLang]) {
        return urlLang;
    }
    
    // 2. Check localStorage
    const savedLang = localStorage.getItem('siteLang');
    if (savedLang && LANDING_LANGUAGES[savedLang]) {
        return savedLang;
    }
    
    // 3. Check browser language
    const browserLang = navigator.language?.slice(0, 2);
    if (browserLang && LANDING_LANGUAGES[browserLang]) {
        return browserLang;
    }
    
    return 'en';
}

